    "columns": ["DOCUMENT_ID", "RELATIVE_PATH", "CHUNK_TYPE", "CHUNK_INDEX", "INVOICE_NUMBER", "CUSTOMER_NAME", "DUE_DATE", "TOTAL_DUE"],
