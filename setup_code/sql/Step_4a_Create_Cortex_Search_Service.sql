CREATE OR REPLACE CORTEX SEARCH SERVICE INVOICE_SEARCH_SVC2
ON CHUNK_TEXT
ATTRIBUTES
  DOCUMENT_ID,
  RELATIVE_PATH,
  CHUNK_TYPE,
  CHUNK_INDEX,
  INVOICE_NUMBER,
  CUSTOMER_NAME,
  DUE_DATE,
  TOTAL_DUE
WAREHOUSE = FIN_ENT_APP_POC2_WH
TARGET_LAG = '5 minutes'
INITIALIZE = ON_CREATE
AS
SELECT
    CHUNK_TEXT,
    DOCUMENT_ID,
    RELATIVE_PATH,
    CHUNK_TYPE,
    CHUNK_INDEX,
    INVOICE_NUMBER,
    CUSTOMER_NAME,
    DUE_DATE,
    TOTAL_DUE
FROM FIN_ENT_AI_POC2.CURATED_DOCS.INVOICE_CHUNK;

