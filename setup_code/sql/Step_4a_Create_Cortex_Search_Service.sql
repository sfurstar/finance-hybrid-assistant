SELECT
    c.CHUNK_TEXT,
    c.DOCUMENT_ID,
    c.RELATIVE_PATH,
    c.CHUNK_INDEX,
    e.INVOICE_NUMBER,
    e.CUSTOMER_NAME,
    e.DUE_DATE,
    e.TOTAL_DUE
FROM FIN_ENT_AI_POC2.CURATED_DOCS.INVOICE_CHUNK c
LEFT JOIN FIN_ENT_AI_POC2.CURATED_DOCS.INVOICE_EXTRACT e
  ON c.DOCUMENT_ID = e.DOCUMENT_ID
LIMIT 20;